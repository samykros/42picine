#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	start;
	int	end;
	int	temp;

	start = 0;
	end = size - 1;
	while (start < end)
	{
		temp = tab[start];
		tab[start] = tab[end];
		tab[end] = temp;
		start++;
		end--;
	}
}

int main(void)
{
    int tab[3] = {1, 2, 3};
    int *ptr = tab;

    ft_rev_int_tab(ptr, 3);
    printf("Reversed vector: %d %d %d", tab[0], tab[1], tab[2]);
    return 0;
}
